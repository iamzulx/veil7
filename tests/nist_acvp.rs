/// NIST ACVP ML-KEM-768 KeyGen Test Vector #2 (determinism + size check)
#[test]
fn nist_acvp_mlkem768_keygen_vector_2() {
    let z = "DF0F282411F4A071489A8F618E2AE5AEF40131CAC5233D6D731522720C2FEB1C";
    let d = "444F032DD19AE7518C4B35B0732A41DC567845ABA8BD7B04A9C413A0CF2DE0B5";

    let seed_hex = format!("{d}{z}");
    let seed = hex_decode_array::<64>(&seed_hex);

    let kp = kem_keygen(seed);

    // Verify key sizes match FIPS 203 specification
    assert_eq!(
        kem_pk_bytes(&kp).len(),
        KEM_PK_SIZE,
        "ML-KEM-768 pk must be 1184 bytes"
    );
    assert_eq!(
        kem_sk_bytes(&kp).len(),
        KEM_SK_SIZE,
        "ML-KEM-768 sk must be 2400 bytes"
    );

    // Determinism: same seed must produce same keys
    let kp2 = kem_keygen(seed);
    assert_eq!(kem_pk_bytes(&kp), kem_pk_bytes(&kp2));
    assert_eq!(kem_sk_bytes(&kp), kem_sk_bytes(&kp2));

    // Must differ from vector #1
    let seed1_hex = format!(
        "{}{}",
        "E34A701C4C87582F42264EE422D3C684D97611F2523EFE0C998AF05056D693DC",
        "A85768F3486BD32A01BF9A8F21EA938E648EAE4E5448C34C3EB88820B159EEDD"
    );
    let seed1 = hex_decode_array::<64>(&seed1_hex);
    let kp1 = kem_keygen(seed1);
    assert_ne!(
        kem_pk_bytes(&kp),
        kem_pk_bytes(&kp1),
        "different seeds must produce different keys"
    );
}

// ═══════════════════════════════════════════════════════════════════════════
// Cross-validation: NIST vectors → sign → verify roundtrip
// ═══════════════════════════════════════════════════════════════════════════

/// Use NIST keygen vector to produce keys, then sign and verify.
#[test]
fn nist_acvp_mldsa65_keygen_then_sign_verify() {
    let seed =
        hex_decode_array::<32>("70CEFB9AED5B68E018B079DA8284B9D5CAD5499ED9C265FF73588005D85C225C");

    let kp = dsa_keygen(seed);

    // Sign a test message
    let msg = b"NIST ACVP cross-validation test message";
    let ctx = b"veil7:nist-acvp:v1";
    let randomness = [0x42; 32];

    let sig = dsa_sign(&kp.signing_key, msg, ctx, randomness)
        .expect("signing with NIST-derived key must succeed");

    // Verify
    let result = dsa_verify(&kp.verification_key, msg, ctx, &sig);
    assert!(
        result.is_ok(),
        "verification of signature from NIST-derived key must succeed"
    );
}

/// Use NIST KEM keygen vector to produce keys, then encapsulate/decapsulate.
#[test]
fn nist_acvp_mlkem768_keygen_then_kem_roundtrip() {
    let z = "A85768F3486BD32A01BF9A8F21EA938E648EAE4E5448C34C3EB88820B159EEDD";
    let d = "E34A701C4C87582F42264EE422D3C684D97611F2523EFE0C998AF05056D693DC";
    let seed_hex = format!("{d}{z}");
    let seed = hex_decode_array::<64>(&seed_hex);

    let kp = kem_keygen(seed);
    let pk = kp.public_key();
    let sk = kp.private_key();

    // Encapsulate
    let enc_randomness = [0xBB; 32];
    let (ct, ss_send) = kem_encapsulate(pk, enc_randomness);

    // Decapsulate
    let ss_recv = kem_decapsulate(sk, &ct);

    assert_eq!(
        ss_send.as_slice(),
        ss_recv.as_slice(),
        "KEM roundtrip with NIST-derived keys must produce matching shared secrets"
    );
}
